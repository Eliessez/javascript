// Déclaration de variable 
let uneVariable;

// Affectation d'une variable (donne une valeur à la variable)
uneVariable = 'Je suis une variable'

// Les deux peuvent se faire en meme temps 
let hello = 'Hello , World!'
hello = uneVariable
console.log(hello);
// hello prend la valeur de uneVariable

// le mot cle const permet de déclarer une constante
// Vous ne pouvez pas modifier une constante (immuable)
// 'const' va garantir que la valeur ne soit pas modifié tout au long de l'execution du script
const constante = 'je suis une constante '

// Le  mot clé var permet aussi de déclarer des variables 
// C'est une pratique désuète (c'etait la seule facon en 2015)
var past = 'je suis depassé' 
